package com.rapidhelp.models;

public class DirectUserReport {

    private String name,email,phone,regDate,userPackage,provideHelpStatus,provideHelpDate;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getRegDate() {
        return regDate;
    }

    public void setRegDate(String regDate) {
        this.regDate = regDate;
    }

    public String getUserPackage() {
        return userPackage;
    }

    public void setUserPackage(String userPackage) {
        this.userPackage = userPackage;
    }

    public String getProvideHelpStatus() {
        return provideHelpStatus;
    }

    public void setProvideHelpStatus(String provideHelpStatus) {
        this.provideHelpStatus = provideHelpStatus;
    }

    public String getProvideHelpDate() {
        return provideHelpDate;
    }

    public void setProvideHelpDate(String provideHelpDate) {
        this.provideHelpDate = provideHelpDate;
    }
}
