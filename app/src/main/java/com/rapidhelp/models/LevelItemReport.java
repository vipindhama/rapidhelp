package com.rapidhelp.models;

public class LevelItemReport {
    private String name,userId,regDate,provideHelpStatus,provideHelpDate;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getRegDate() {
        return regDate;
    }

    public void setRegDate(String regDate) {
        this.regDate = regDate;
    }

    public String getProvideHelpStatus() {
        return provideHelpStatus;
    }

    public void setProvideHelpStatus(String provideHelpStatus) {
        this.provideHelpStatus = provideHelpStatus;
    }

    public String getProvideHelpDate() {
        return provideHelpDate;
    }

    public void setProvideHelpDate(String provideHelpDate) {
        this.provideHelpDate = provideHelpDate;
    }
}
